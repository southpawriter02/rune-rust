namespace RuneAndRust.Core.Enums;

/// <summary>
/// Represents the available options on the main menu (v0.3.4a).
/// </summary>
public enum MainMenuOption
{
    /// <summary>
    /// Start a new game with character creation.
    /// </summary>
    NewGame = 0,

    /// <summary>
    /// Load an existing saved game.
    /// </summary>
    LoadGame = 1,

    /// <summary>
    /// Access game options and settings.
    /// </summary>
    Options = 2,

    /// <summary>
    /// Exit the application.
    /// </summary>
    Quit = 3
}
